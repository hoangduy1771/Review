package review20241;

public class WrapperClass {
    public static void main(String[] args) {
//        Wrapper class is a way to use primitive data type as reference data types
//        reference data types have more useful methods
//        and it can be used with collections (Ex. ArrayList)

//        primitive         //  wrapper (reference data type)
//        ---------         // --------
//        boolean           // Boolean
//        char              // Character
//        int               // Int
//        double            // Double

//        autoboxing = automatic conversion of assigning primitive data types to reference data types
//        unboxing = reverse of autoboxing - automatic

//        autoboxing - automatic convert reference types to primitive types upon assigning
        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Rusty";


        a.equals(true);







    }
}
